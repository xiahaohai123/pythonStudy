"""

数值  1、int整型  2、float浮点型
布尔型 1、True  2、False
str(字符串)
list(列表)
tuple(元组)
set(集合)
dict(字典)

"""

# 整型 int
num = 1
print(type(num))

# 浮点型 float
num = 2.2
print(type(num))

# 布尔型 bool
flag = True
print(type(flag))

# 字符串 str
str1 = "张三"
print(type(str1))

# 列表 list
lis = [2, 3, 4]
print(type(lis))
print(lis)

# 元组 tuple
t = (2, 4, 6)
print(type(t))
print(t)

# 集合 set
s = {2, 3, 4}
print(type(s))
print(s)

# 字典 dict
d = {"age": 18}
print(type(d))
print(d)
